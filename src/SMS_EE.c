void* SMS_Realloc ( void* apData, unsigned int* apSize, unsigned int aMinSize ) {

 if ( aMinSize < *apSize ) return apData;

 *apSize = 17 * aMinSize / 16 + 32;

 return realloc ( apData, *apSize );

}  /* SMS_Realloc */

void* SMS_ReallocWithAlign ( void* apData, unsigned int* apSize, unsigned int aMinSize ) {

 unsigned int lnOldSize;
 void *lpNewData;

 lnOldSize = *apSize;

 if ( apData != NULL && aMinSize < lnOldSize ) return apData;
 if ( aMinSize == 0 ) return apData;

 if ( apData == NULL ) {
  apData = memalign( 64, aMinSize );
 } else {
  apData = realloc( apData, aMinSize );
  if ( (((unsigned int)apData) & 63) != 0 ) {
   // We got misaligned, so need to allocate to aligned portion
   apData = realloc( apData, lnOldSize );
   lpNewData = memalign( 64, aMinSize );
   if ( lpNewData != NULL ) {
   	memcpy( lpNewData, apData, lnOldSize );
   }
   free( apData );
   apData = lpNewData;
  }
 }

 if ( apData == NULL ) {
  aMinSize = 0;
 }

 *apSize = aMinSize;

 return apData;

}  /* SMS_ReallocWithAlign */

extern s64  MUL64 ( s64 , s64  );

s64  SMS_Rescale ( s64  anA, s64  aB, s64  aC ){

 int           i;
 u64           lA0, lA1, lB0, lB1, lT1, lT1a;
 s64           lR;

 if ( anA < 0 ) return -SMS_Rescale ( -anA, aB, aC );

 lR = aC >> 1;

 if ( aB <= INT_MAX && aC <= INT_MAX ) {
  if ( anA <= INT_MAX )
   return ( anA * aB + lR ) / aC;
  else return anA / aC * aB + ( anA % aC * aB + lR ) / aC;
 }  /* end if */

 lA0  = anA & 0xFFFFFFFF;
 lA1  = anA >> 32;
 lB0  = aB  & 0xFFFFFFFF;
 lB1  = aB  >> 32;
 lT1  = MUL64 ( lA0, lB1 ) + MUL64 ( lA1, lB0 );
 lT1a = lT1 << 32;
 lA0  = MUL64 ( lA0, lB0 ) + lT1a;
 lA1  = MUL64 ( lA1, lB1 ) + ( lT1 >> 32 ) + ( lA0 < lT1a );
 lA0 += lR;
 lA1 += lA0 < lR;

 for ( i = 63; i >= 0; --i ) {

  lA1 += lA1 + (  ( lA0 >> i ) & 1  );
  lT1 += lT1;

  if ( aC <= lA1 ){
   lA1 -= aC;
   ++lT1;
  }  /* end if */

 }  /* end for */

 return lT1;

}  /* end SMS_Rescale */

static char UTIL_getReversedChar ( char aChr ) {

 switch ( aChr )  {
  case '(': return ')';
  case ')': return '(';
  case '[': return ']';
  case ']': return '[';
  case '{': return '}';
  case '}': return '{';
  case '<': return '>';
  case '>': return '<';
 }  /* end switch */

 return aChr;

}  /* end UTIL_getReversedChar */

char* SMS_ReverseString ( char* apStr, int aLen ) {

 unsigned char lRev[ aLen ];
 unsigned char lEON[ aLen ];
 int           lSrcIdx;
 int           lEONIdx = 0;
 int           lRevIdx = 0;

 for ( lSrcIdx = aLen - 1; lSrcIdx >= 0; --lSrcIdx ) {

  char lChr = apStr[ lSrcIdx ];

  if (  isalnum ( lChr )  )

   lEON[ lEONIdx++ ] = lChr;

  else {

   if ( lEONIdx > 0 ) {

    for ( lEONIdx = lEONIdx - 1; lEONIdx >= 0; --lEONIdx ) lRev[ lRevIdx++ ] = lEON[ lEONIdx ];

    lEONIdx = 0;

   }  /* end if */

   lRev[ lRevIdx++ ] = UTIL_getReversedChar ( lChr );

  }  /* end else */

 }  /* end for */

 if ( lEONIdx > 0 ) for ( lEONIdx = lEONIdx - 1; lEONIdx >= 0; --lEONIdx ) lRev[ lRevIdx++ ] = lEON[ lEONIdx ];

 for ( lRevIdx = 0; lRevIdx < aLen; ++lRevIdx ) apStr[ lRevIdx ] = lRev[ lRevIdx ];

 return apStr;

}  /* end SMS_ReverseString */

